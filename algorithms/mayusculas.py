def transform_to_uppercase(sequence1, sequence2):
    result = {
        'sequence1_upper': sequence1.upper(),
        'sequence2_upper': sequence2.upper()
    }
    return result
def transform_to_lowecase(sequence1, sequence2):
    result = {
        'sequence1_upper': sequence1.lower(),
        'sequence2_upper': sequence2.lower()
    }
    return result